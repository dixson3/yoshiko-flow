"""Tests for _shared/sync.py — the repo-time `_shared/` vendoring sync tool.

Run:  uv run --with pytest python3 -m pytest test_sync.py -q

Covers both vendoring modes (plan-016):
  * region-vendored assets (active-set classifier, defensive json extractor) — a
    marker-fenced region copied into larger consumer files;
  * whole-file assets (manifest_update.py) — a canonical file copied verbatim.

The pure functions are exercised against in-memory fixtures and tmp_path files. The
main() integration tests monkeypatch the module's REGION_ASSETS / WHOLE_FILE_ASSETS /
REPO_ROOT to point at temp files, so they never touch the real skill scripts.
"""
import importlib.util
import re
import sys
from pathlib import Path

import pytest

_spec = importlib.util.spec_from_file_location("sync", Path(__file__).parent / "sync.py")
sync = importlib.util.module_from_spec(_spec)
sys.modules["sync"] = sync
_spec.loader.exec_module(sync)


# Active-set markers (used for the generic region-function tests).
ACTIVE_STEM_BEGIN = ">>> BEGIN active-set classifier"
ACTIVE_STEM_END = "<<< END active-set classifier"
BEGIN_RE = re.compile(re.escape(ACTIVE_STEM_BEGIN))
END_RE = re.compile(re.escape(ACTIVE_STEM_END))

CONSUMER_BEGIN = "# >>> BEGIN active-set classifier (generated by _shared/sync.py — do not edit) >>>"
CONSUMER_END = "# <<< END active-set classifier <<<"


def make_consumer(body: str, *, before="import os\n", after="\ndef tail():\n    pass\n") -> str:
    """Build a fake consumer script carrying the generated-by marker fence around `body`."""
    return f"{before}{CONSUMER_BEGIN}\n{body}{CONSUMER_END}\n{after}"


def _region_asset(canonical, consumers):
    return sync.RegionAsset(
        name="t-region",
        canonical=canonical,
        begin=ACTIVE_STEM_BEGIN,
        end=ACTIVE_STEM_END,
        consumers=consumers,
    )


